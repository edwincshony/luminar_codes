print("----------Program for Calculating Simple Interest----------")

principal_amount = 100000

rate = 8.25

time_period = 3

simple_interest = (principal_amount * rate * time_period) / 100

print("Simple interest for",principal_amount,"rs for",time_period,"years at",rate,"interest rate is",simple_interest,"rs") #my version

print("The simple interest for", principal_amount, "rupees for", time_period, "years at an interest rate of", rate, "is", simple_interest, "rupees.") # chatgpt

print(f"The simple interest on {principal_amount} rupees for {time_period} years at an interest rate of {rate}% is {simple_interest} rupees.") # chatgpt fstring
